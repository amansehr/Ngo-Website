<h1 align="justify">About YVCK</h1>
  <p> Yuva Vikash Club was established in the year 2017 with the objective of providing rural youth avenues to take part in the process of nation building as well providing opportunities for the development of their personality and skills.</p>
  <p>In the year 2017-2018, Yuva Vikash Club Kalirawan(YVCK) was set up as an autonomous organization under the Government of India, Ministry of Youth Affairs and Sports,  to oversee working of these Kendras. YVCK  is the largest grassroots level 
      youth organization; one of its kind in the world.  It  channelizes the power of youth on the principles of voluntarism, self-help and  community participation.</p>
  <p>Over the years, Yuva Vikash Club Kalirawan has established a network of youth clubs in villages, where Yuva Vikash Clubs have been set up. YVCK has targeted to identify areas of harnessing youth power for development by forming Youth Clubs, which are village level voluntary action groups of youth at the grassroots level to involve them in nation building activities. The core strength of YVCK lies in its network of youth clubs. Youth Clubs are village based organizations working for community development and youth <br />
    empowerment.</p>
  <p>&nbsp;The basic objective for creation of youth clubs is to render community support through developmental initiatives involving activities with particular focus on youth empowerment. The implementation of programmes and activities of youth clubs is based on local needs and requirements by mobilizing resources from various government departments and other agencies, which include both national, State level and multilateral institutions. The youth clubs and its member volunteers form the base of the YVCKâ€™s vast national rural network.</p>
  <p><strong>Objectives :    </strong></p>
  <p><strong>Objectives of YVCK are two fold :-</strong></p>
  <ul class="lists">
    <li> To involve the rural youth in nation building activities.</li>
    <li>To develop such skills and values in them with which they become responsible and productive citizens of a   modern, secular and technological nation.
      
      <br />
      Yuva Vikash Club has been working in various fronts of youth development with a variety of youth programmes of the Ministry of Youth Affairs and certain special programmes in coordination and cooperation of other ministries. Main focus has been on developing values of good citizenship, thinking and behaving in secular ways, skill development and helping youth to adopt a productive and organized behaviour.      </li>
  </ul>
  <p class="clear"><strong>Vision :</strong> </p>
  <p>Vision of the organization focuses on developing long term strategies for good citizenship and youth leadership at the grass root level. Youth Clubs are formed and encouraged to participate in sports, cultural and local development activities. Youth leadership is developed in the course of formation and sustenance of youth clubs. This leadership becomes highly useful in creating:-    </p>
  <ul class="lists">
    <li>Networks of volunteership.      </li>
    <li>Oppurtunities of participation in fundamental democratic practices of polity and development; and,</li>
    <li>Instruments of empowerment of youth like skill-generation, awareness creation about health, life skills,   and self employment.      </li>
  </ul>
  <p>Considering the fact that almost three-fourth of the Indian population is rural, the development of the nation as a whole depends on their progress and development. Besides, the demographic dividend that the country has enjoyed because of the larger segment of youth population, makes it almost mandatory 
      for the largest youth organization like YVCK to take up more and more of such programmes as will promise 
      empowerment of youth. </p>
